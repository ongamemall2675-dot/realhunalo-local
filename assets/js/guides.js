/**
 * Get guide content for a specific module
 * @param {string} moduleId - Module ID (script, image, motion, tts, video, shorts, etc.)
 * @returns {Object|null} - Guide object or null if not found
 */
export function getGuide(moduleId) {
    return ModuleGuides[moduleId] || null;
}

/**
 * Check if a module has a guide
 * @param {string} moduleId - Module ID
 * @returns {boolean} - True if guide exists
 */
export function hasGuide(moduleId) {
    return moduleId in ModuleGuides;
}
